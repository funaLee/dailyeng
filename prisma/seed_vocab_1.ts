// Seed function
async function seedVocab() {
  console.log("üå± Seeding Vocabulary Data - Daily Life...");

  // Get or create topic group
  const topicGroup = await prisma.topicGroup.upsert({
    where: { name_hubType: { name: "Daily Life", hubType: "vocab" } },
    update: {},
    create: {
      name: "Daily Life",
      order: 1,
      hubType: "vocab",
      subcategories: [
        "Home Living",
        "Food & Drinks",
        "Shopping",
        "Transportation",
      ],
    },
  });

  // Helper to slugify
  const slugify = (text: string) =>
    text.toLowerCase().replace(/&/g, "and").replace(/\s+/g, "-");

  // Iterate over all subcategories in dailyLifeVocab
  for (const [subcat, levelsData] of Object.entries(dailyLifeVocab)) {
    console.log(`Processing Subcategory: ${subcat}`);

    // Iterate over levels present in this subcategory
    for (const [level, vocabItems] of Object.entries(levelsData)) {
      // Loop over levels provided
      const currentLevel = level as Level;
      const items = vocabItems as ReturnType<typeof v>[]; // Cast to correct type

      if (!items || items.length === 0) continue;

      const topicId = `daily-life-${slugify(
        subcat
      )}-${currentLevel.toLowerCase()}`;

      const topic = await prisma.topic.upsert({
        where: { id: topicId },
        update: { wordCount: items.length },
        create: {
          id: topicId,
          title: `${subcat} - ${currentLevel}`,
          subtitle: `Vocabulary about ${subcat.toLowerCase()}`,
          description: `Learn essential vocabulary about ${subcat.toLowerCase()} at ${currentLevel} level.`,
          level: currentLevel,
          wordCount: items.length,
          estimatedTime: Math.ceil(items.length * 2),
          category: "Daily Life",
          subcategory: subcat,
          order: LEVELS.indexOf(currentLevel),
          topicGroupId: topicGroup.id,
        },
      });

      // Seed vocab items
      for (const vocab of items) {
        const vocabId = `${topic.id}-${vocab.word
          .toLowerCase()
          .replace(/\s+/g, "-")}`;
        await prisma.vocabItem.upsert({
          where: { id: vocabId },
          update: vocab,
          create: {
            id: vocabId,
            topicId: topic.id,
            ...vocab,
          },
        });
      }
      console.log(
        `‚úÖ Created: ${subcat} - ${currentLevel} (${items.length} words)`
      );
    }
  }

  console.log("‚úÖ Daily Life seeded successfully!");
}

async function main() {
  try {
    await seedVocab();
  } catch (error) {
    console.error("‚ùå Error:", error);
    throw error;
  } finally {
    await prisma.$disconnect();
  }
}

main();
